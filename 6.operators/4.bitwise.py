'''
bitwise operators are performed only on integers.
'''

a = 10
b = 4

print("Bitwise and operator: ", a & b)

print("Bitwise or operator: ", a | b)

print("Bitwise xor operator: ", a ^ b)

print("Bitwise not operator: ", ~b ) 

print("Bitwise shift right operator: ", a >> 1)

print("Bitwise shift left operator: ", a << 1)